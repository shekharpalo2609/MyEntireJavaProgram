package assignment;

//How to use substring 
public class Substring_59 {

	public static void main(String[] args) {
		
		String name = "My name is Shekhar";
		
		//1 parameter substring
		System.out.println(name.substring(11));
		
		//2 parameter substring
		System.out.println(name.substring(1, name.length()-2));
	}

}
