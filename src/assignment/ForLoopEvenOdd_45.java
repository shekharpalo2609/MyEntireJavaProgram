package assignment;

//Write For loop for even or odd numbers 2 programs 
public class ForLoopEvenOdd_45 {

	public static void main(String[] args) {
		// For even number:
		for (int i = 2; i < 101; i++) {
			System.out.println(i);
			i++;
		}
		System.out.println("----------------------");
		// For odd number:
		for (int i = 1; i < 101; i++) {
			System.out.println(i);
			i++;
		}

	}

}
